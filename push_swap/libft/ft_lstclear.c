#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	void	*ptr;

	if (!lst || !del)
		return ;
	while (*lst)
	{
		ptr = (*lst)->next;
		ft_lstdelone(*lst, del);
		*lst = ptr;
	}
}
